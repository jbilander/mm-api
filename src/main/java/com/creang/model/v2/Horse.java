package com.creang.model.v2;

public class Horse {

    private String name;
    private int age;
    private String gender;
    private int foreShoes;
    private int hindShoes;
    private String sire;
    private String dam;
    private String damSire;
    private String homeTrack;
    private String color;
    private String blinkerType;
    private String rating;
    private Driver driver;
    private Trainer trainer;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getForeShoes() {
        return foreShoes;
    }

    public void setForeShoes(int foreShoes) {
        this.foreShoes = foreShoes;
    }

    public int getHindShoes() {
        return hindShoes;
    }

    public void setHindShoes(int hindShoes) {
        this.hindShoes = hindShoes;
    }

    public String getSire() {
        return sire;
    }

    public void setSire(String sire) {
        this.sire = sire;
    }

    public String getDam() {
        return dam;
    }

    public void setDam(String dam) {
        this.dam = dam;
    }

    public String getDamSire() {
        return damSire;
    }

    public void setDamSire(String damSire) {
        this.damSire = damSire;
    }

    public String getHomeTrack() {
        return homeTrack;
    }

    public void setHomeTrack(String homeTrack) {
        this.homeTrack = homeTrack;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getBlinkerType() {
        return blinkerType;
    }

    public void setBlinkerType(String blinkerType) {
        this.blinkerType = blinkerType;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public Driver getDriver() {
        return driver;
    }

    public void setDriver(Driver driver) {
        this.driver = driver;
    }

    public Trainer getTrainer() {
        return trainer;
    }

    public void setTrainer(Trainer trainer) {
        this.trainer = trainer;
    }
}
